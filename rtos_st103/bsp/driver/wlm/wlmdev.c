/**
 ******************************************************************************
 * @brief      WLM初始化
 * @param[in]  None
 * @param[out] None
 * @retval     None
 *
 * @details
 *
 * @note
 ******************************************************************************
 */
status_t
wlm_init(uint8_t addr)
{
#ifdef WLM_IS_CC1101
    return cc1101_init(addr);
#endif
    return ERROR;
}

/**
 ******************************************************************************
 * @brief      wlm发送
 * @param[in]  * pbuf 指向要发送的报文缓存
 * @param[out] None
 * @retval     -1       发送失败
 * @retval     other    发送字节数
 *
 * @details
 *
 * @note
 ******************************************************************************
 */
int32_t
wlm_send(uint8_t addr, const uint8_t* pbuf, int32_t len)
{
#ifdef WLM_IS_CC1101
    return cc1101_send(addr, pbuf, len);
#endif
    return -1;
}

/**
 ******************************************************************************
 * @brief      wlm接收
 * @param[in]  * pbuf 指向要接收的报文缓存
 * @param[out] None
 * @retval     -1       接收失败
 * @retval     other    接收字节数
 *
 * @details
 *
 * @note
 ******************************************************************************
 */
int32_t
wlm_recv(uint8_t addr, uint8_t* pbuf, int32_t len)
{
#ifdef WLM_IS_CC1101
    return cc1101_recv(addr, pbuf, len);
#endif
    return -1;
}
